GameMap::GameMap() {
	for (int row = 0; row < MAP_HEIGHT; row++) {
        for (int col = 0; col < MAP_WIDTH; col++) {
            this->pacManMap[row][col] = pacman_map[row][col];
        }
    }
}

void GameMap::resetMap() {
    for (int row = 0; row < MAP_HEIGHT; row++) {
        for (int col = 0; col < MAP_WIDTH; col++) {
            this->pacManMap[row][col] = pacman_map[row][col];
        }
    }
}

void GameMap::removePebble(int row, int col) {
    pacManMap[row][col] = 0;
}

